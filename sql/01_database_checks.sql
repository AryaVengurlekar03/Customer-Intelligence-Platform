-- Total Customers
SELECT COUNT(*) AS total_customers
FROM customers;

-- Total Orders
SELECT COUNT(*) AS total_orders
FROM orders;

-- Total Products
SELECT COUNT(*) AS total_products
FROM products;

-- Total Sellers
SELECT COUNT(*) AS total_sellers
FROM sellers;

-- Total Payments
SELECT COUNT(*) AS total_payments
FROM payments;

-- Total Reviews
SELECT COUNT(*) AS total_reviews
FROM reviews;